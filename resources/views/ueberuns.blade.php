        <h1>Damals wie heute</h1>
        <p>
            Frische, Geschmack, ein Sortiment, das keine Wünsche offen lässt, und ein Service, der sich stets am Kunden orientiert: 
            Das war Werner Eberts Vision, als er 1979 sein Unternehmen auf dem Stuttgarter Großmarkt eröffnete.

            Diese Linie gilt heute wie damals – und so bleibt es auch.

            Qualität ohne Kompromisse. Selbst eine Sterne-Küche ist nur so gut wie ihr Lieferant. Deshalb sind Frische und Qualität bei uns oberstes Gebot – ob bei Obst, Gemüse, exotischen Südfrüchten oder Trockenfrüchten.
            Um diesem Anspruch das ganze Jahr über gerecht zu werden, importieren wir handverlesene Waren aus aller Welt – wenn es sein muss direkt aus den berühmten Pariser Markthallen (Rungis).

            Qualität zählt nicht nur beim Produkt, sondern auch beim Service: Unser eigener Fuhrpark mit Lieferservice stellt sicher, dass unsere Ware besonders frisch und pünktlich bei Ihnen ankommt – bei Bedarf küchenfertig aufbereitet.

